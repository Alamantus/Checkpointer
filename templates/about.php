<h2>About</h2>
<p>Checkpointer is a nested checklist application that is intended to help you reach your larger goals by breaking them down into smaller checkpoints that you can reach more easily.</p>
<p>The original inspiration for this project was this video from the entertaining and insightful people over at Extra Credits:</p>
<p>
    <iframe width="560" height="315" src="https://www.youtube.com/embed/dHMNeNapL1E" frameborder="0" allowfullscreen></iframe>
</p>
<p>Among many other helpful bits of information, they mentioned that breaking down your goals into smaller, more manageable pieces is a helpful way to keep your projects from becoming too overwhelming while at the same time allowing you to keep track of everything that you actually need to get done.</p>
